package com.haidong.storysharing.entry;

public class StoryInfoUser {
    private Long id;

    private Long storyInfoId;

    private Long userId;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getStoryInfoId() {
        return storyInfoId;
    }

    public void setStoryInfoId(Long storyInfoId) {
        this.storyInfoId = storyInfoId;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }
}